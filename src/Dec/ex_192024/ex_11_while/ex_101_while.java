package Dec.ex_192024.ex_11_while;

public class ex_101_while {
    public static void main(String[] args) {

        int modi  = 0 ;
        while(modi <= 10){
            modi ++ ;
            System.out.println("modi 10 years");
        }
    }

    public static class ex_11_while {
        public static void main(String[] args) {

            int i = 0 ;
            while(i > 10){
                System.out.println(i);
                i++;
            }

            for (int j = 0 ; j< 10 ; j++){
                System.out.println(j);
            }
        }
    }
}
