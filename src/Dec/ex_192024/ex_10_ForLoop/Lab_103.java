package Dec.ex_192024.ex_10_ForLoop;

public class Lab_103 {

    public static void main(String[] args) {


        for (int rohit = 1; rohit <= 10; ++rohit) {
            System.out.println(rohit); // 11 times, print 0 to 10
        }

        for (int _1 = 0; _1 <= 10; _1++) { // 11 times
            System.out.println(_1); // 0, to 10
        }


    }
}